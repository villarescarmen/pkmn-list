document.getElementById('pokemon-form').addEventListener('submit', function(event) {
    event.preventDefault();
    const inputName = document.getElementById('pokemon-name').value.trim();
    const resultDiv = document.getElementById('result');
    
    if (inputName) {
        const isPokemonFirstGen = pokemonList.some(pokemon => pokemon.toLowerCase() === inputName.toLowerCase());
        if (isPokemonFirstGen) {
            resultDiv.textContent = `${inputName} es un Pokémon de 1ª generación.`;
            resultDiv.style.color = 'green';
        } else {
            resultDiv.textContent = `${inputName} no es un Pokémon de 1ª generación, o puede que no está bien escrito.`;
            resultDiv.style.color = 'red';
        }
    } else {
        resultDiv.textContent = 'Por favor, ingresa un nombre de Pokémon de primera Generación (nº 1 al 151).';
        resultDiv.style.color = 'orange';
    }

    
});


